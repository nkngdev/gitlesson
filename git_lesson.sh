git clone  # копирует удаленный репозиторий на локальный компьютер.
git init  # создает локальный репозиторий в текущей директории.
git add  # добавляет файлы в индекс, который будет затем зафиксирован в коммите.
git commit  # создает коммит, сохраняя текущие изменения в индексе.
git push  # отправляет коммиты из локального репозитория в удаленный.
git pull  # получает коммиты из удаленного репозитория и обновляет локальный репозиторий